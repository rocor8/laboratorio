    public void MostrarHistoriaClinica (){
        
        String historia = null;
                
        for(Paciente pac : clinica.getPacientes() ){
            if ( pac.getDni().equals(dni)){
                 historia = pac.toString() + "\nDoctor: " +pac.getDoctor() + "\nEspecialidad: "+pac.getEspecialidad() + "\nHabitacion: "+pac.getHabitacion() + "\nNumero de cama: "+pac.getHabitacion().getCama(dni) + "\n\n------Diagnostico------ " +pac.getDiagnostico();
                break;
            }
        }
        
        jTextArea1.setText(historia);
    }
    
