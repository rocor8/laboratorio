    public String[] getHabitacion(Object especialidad){        
        String[] resultado = null;
        for (Especialidad esp : clinica.getEspecialidades()){
            if(esp.getNombre().equals(especialidad.toString())){
                resultado = esp.getHabitacion();
            }
        }
        return resultado;        
    }
    public void getDoctores(Object especialidad){        
        comboBox_Doctores.removeAllItems();
        for (Doctor doc : clinica.getDoctores()){
            if(doc.getEspecialidad().getNombre().equals((especialidad.toString()))){
            comboBox_Doctores.addItem(doc.toString());
            }
        }       
    }
    public String[] getCamas(Object habitacion){        
        String[] resultado = null;
        for (Habitacion hab : clinica.getHabitaciones()){
            if(hab.toString().equals(habitacion.toString())){
                resultado = hab.setCamasLibre();
            }
        }
        return resultado;        
    }
    public String[] getDias(int mes){
        int i;
        String[] dias;
        
        if (mes == 2){
            dias = new String[28];
            for(i=1;i<29;i++)
            dias [i-1] = ""+i;
        }
        else if(mes==11 || mes==4 || mes == 6 || mes==9){
            dias = new String[30];
            for(i=1;i<31;i++)
            dias [i-1] = ""+i;
            }
        else{
            dias = new String[31];
            for(i=1;i<32;i++)
            dias [i-1] = ""+i;
        }
     return dias;       
    }
