    public void cagarComboBoxPorDefecto(){
        int i;
        for(i=1;i<13;i++)
            comboBox_Mes.addItem(""+i);
        for(i=2017;i>=1900;i--)
            comboBox_Anio.addItem(""+i);
                
        for (Especialidad esp : clinica.getEspecialidades()){
            comboBox_Especialidad.addItem(esp.getNombre());
        }
        
    }
    public void cargarDatosPaciente(){
        String fNac=null;
        for(Paciente pac : clinica.getPacientes()){
            if(pac.getDni().equals(dniModificar)){
                text_Nombre.setText(pac.getNombre());
                text_Apellido.setText(pac.getApellido());
                text_Documento.setText(pac.getDni());
                text_Direccion.setText(pac.getDireccion());
                comboBox_Sexo.setSelectedItem(pac.getSexo());
                comboBox_Especialidad.setSelectedItem(pac.getEspecialidad().toString());
                comboBox_NHabitacion.setSelectedItem(pac.getHabitacion().toString());
                comboBox_Doctores.setSelectedItem(pac.getDoctor().toString());
                comboBox_NCama.addItem(pac.getHabitacion().getCama(dniModificar));
                comboBox_NCama.setSelectedItem(pac.getHabitacion().getCama(dniModificar));
                fNac = pac.getFechaDeNaciemiento();
            }
        }
        int i;
        int dia = Integer.parseInt(fNac.substring(0,2));
        int mes = Integer.parseInt(fNac.substring(3,5));
        int anio = Integer.parseInt(fNac.substring(6,10));
        comboBox_Anio.setSelectedItem(""+anio);
        comboBox_Mes.setSelectedItem(""+mes);
        comboBox_Dia.setSelectedItem(""+dia);
    }
