    private void jButton_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_GuardarActionPerformed
         try {
             
        String nomb = text_Nombre.getText().toUpperCase();
        String ape = text_Apellido.getText().toUpperCase();
        String sex = comboBox_Sexo.getSelectedItem().toString();
        String doc = text_Documento.getText();
        String domi = text_Direccion.getText().toUpperCase();
        int dia = Integer.parseInt(comboBox_Dia.getSelectedItem().toString());
        int mes = Integer.parseInt(comboBox_Mes.getSelectedItem().toString());
        int anio = Integer.parseInt(comboBox_Anio.getSelectedItem().toString());
        Object especialidad = comboBox_Especialidad.getSelectedItem();
        Object doctor = comboBox_Doctores.getSelectedItem();
        Object habitacion = comboBox_NHabitacion.getSelectedItem();
        int numCama = Integer.parseInt(comboBox_NCama.getSelectedItem().toString());
        clinica.verificarDatos(doc,nomb,ape,domi);
        if(doc.equals(dniModificar)){}
        else
            clinica.buscarRepetido(doc);
        for(Paciente pac : clinica.getPacientes()){
                if(pac.getDni().equals(dniModificar)){
                    pac.setApellido(ape);
                    pac.setDireccion(domi);
                    pac.setDni(doc);
                    pac.setNombre(nomb);
                    pac.setSexo(sex);
                    pac.getHabitacion().bajaPacienteCama(dniModificar);
                    clinica.sincronisarInformacion(pac, doctor, especialidad, habitacion, numCama);
                    JOptionPane.showMessageDialog(null, "Paciente Modificado", "Operacion Exitosa", JOptionPane.INFORMATION_MESSAGE);
                    break;
                }
             }
        for(Habitacion hab : clinica.getHabitaciones()){
            
        }
        this.limpliarTextos();
        MenuPrincipal menu = new MenuPrincipal(clinica);
        menu.setLocationRelativeTo(null);
        menu.setVisible(true);
        dispose(); 
        
       }catch(VerificarDniException vdni){
           JOptionPane.showMessageDialog(null, "Dni Invalido", "Error!", JOptionPane.OK_OPTION);
       }catch(VerficarCampoVacioException cav){
           JOptionPane.showMessageDialog(null, "Ingreso un campo vacio", "Atencion!", JOptionPane.QUESTION_MESSAGE);
       }catch(VerificarRepetidosException bpe){
            JOptionPane.showMessageDialog(null, "Paciente registrado", "Aviso!", JOptionPane.WARNING_MESSAGE);
            
       }catch(NullPointerException npe){
           JOptionPane.showMessageDialog(null, "No selecciono cama", "Atencion!", JOptionPane.WARNING_MESSAGE);
       }
        
    }//GEN-LAST:event_jButton_GuardarActionPerformed

